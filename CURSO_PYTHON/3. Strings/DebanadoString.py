# consiste en mostrar contenido de una cadena de manera segmentada

cadena = 'Este es un ejemplo de Substring (Debanado de cadenas)'

print(len(cadena)) #len devuelve el tamaño de una cadena 53

print(cadena[0]) # Esto printa la letra que haya en el numero que se pone entre corchetes (E)

print(cadena[20 :  ]) # Esto printa desde la letra en la posición 20 hasta el final (e Substring (Debanado de cadenas))

print(cadena[-2]) # Esto printa la palabra última de la frase que esté en esa posición (s)

print(cadena[0:5]) # Esto printa la palabra (Este)

print(cadena[ : 10]) # Esto va a printar desde la E de Este hasta la N de un (Este es un)


