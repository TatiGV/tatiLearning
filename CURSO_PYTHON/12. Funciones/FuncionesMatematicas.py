

import math # es una libreria propia de Python que para que se utilice tiene que estar importada
import random # Proporciona métodos random. es una librería

print(math.pow(10, 2)) #POW  eleva a un numero a un exponente. esto da 100. POW funciona igual sin poner la libreria math ej. print(pow(10, 2))
print(math.sqrt(64)) # SQRT Da la raiz cuadrada con float. esto da 8,0
print(math.isqrt(64)) # lo mismo que la anterior pero con numero entero. esto da 8
print(math.sin(90)) # esto saca el seno funcion trigonometrica. esto da 0.8939966663605579
print(math.cos(90)) # saca el coseno. esto da -0.4480736161291702
print(math.tan(90)) # esto saca la tangente -1.995200412208242
print(math.factorial(5)) # esto lo que hace es el factorial del numero que se ponga que un factorial es: 1 * 2 * 3 * 4.... y asi hasta el numero que se pone. en este caso es 5 que da 120

print(random.randint(1, 100)) # te genera un numero aleatorio entre los números que pongas