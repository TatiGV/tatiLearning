# Convertir una lista de caracteres en una cadena.

# ► Entrada:

# ['a','b','c','d','e','f','g','h','i','j','k','l','m','n','o','p','q','r','s','t']

# ► Salida:
# abcdefghijklmnopqrst

lista = ['a','b','c','d','e','f','g','h','i','j','k','l','m','n','o','p','q','r','s','t']

cadena = ''.join(lista) # .join() convierte en string(cadena) una lista

print(cadena)

