

lista = [1, 2, 3, 4, 5]
print(lista)

lista.append(6) #esto agrega datos al final de la lista [1, 2, 3, 4, 5, 6]
lista.append('Python') # esto añade 'Python' al final de la lista [1, 2, 3, 4, 5, 6, 'Python']
print(lista)

lista.insert(2, 2.5) # inserta el dato que quieres agregar en una posición concreta [1, 2, 2.5, 3, 4, 5, 6, 'Python']
print(lista)

# nombre_de_la_variable.append agrega datos al final de la lista
# nombre_de_la_variable.insert agrega datos en la posición que quieras